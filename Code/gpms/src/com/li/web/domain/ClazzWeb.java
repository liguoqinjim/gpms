package com.li.web.domain;

public class ClazzWeb implements java.io.Serializable{

	private static final long serialVersionUID = 1L;
	private String clazzName;
	private String clazzGrade;
	private String clazzMajor;
	public String getClazzName() {
		return clazzName;
	}
	public void setClazzName(String clazzName) {
		this.clazzName = clazzName;
	}
	public String getClazzGrade() {
		return clazzGrade;
	}
	public void setClazzGrade(String clazzGrade) {
		this.clazzGrade = clazzGrade;
	}
	public String getClazzMajor() {
		return clazzMajor;
	}
	public void setClazzMajor(String clazzMajor) {
		this.clazzMajor = clazzMajor;
	}
	
}
